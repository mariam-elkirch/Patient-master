package com.example.fadfadly.Model;

public class User {
    String name;
    String id;
    String password;
    String email;
    String age;
    String gender;
    String decision;
    String self;
    String family;
    String friends;
    String emotion;
    String work;
    String loss;
    String general;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDecision() { return decision; }

    public void setDecision(String decision) { this.decision = decision; }

    public String getSelf() {
        return self;
    }

    public void setSelf(String self) {
        this.self = self;
    }

    public String getFamily() { return family; }

    public void setFamily(String family) { this.family = family; }

    public String getFriends() { return friends; }

    public void setFriends(String friends) { this.friends = friends; }

    public String getEmotion() { return emotion; }

    public void setEmotion(String emotion) { this.emotion = emotion; }

    public String getWork() { return work; }

    public void setWork(String work) { this.work = work; }

    public String getLoss() { return loss; }

    public void setLoss(String loss) { this.loss = loss; }

    public String getGeneral() { return general; }

    public void setGeneral(String general) { this.general = general; }

    public User() {
    }

}
